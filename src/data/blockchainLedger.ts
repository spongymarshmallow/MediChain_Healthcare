export function getLatestBlocks(count: number = 10): BlockchainTransaction[] {
  return [...blockchainLedger].sort((a, b) => b.blockNumber - a.blockNumber).slice(0, count);
}

export function getTransactionsByType(type: BlockchainTransaction['transactionType']): BlockchainTransaction[] {
  return blockchainLedger.filter(t => t.transactionType === type);
}

export function getTransactionsByPatient(patientId: string): BlockchainTransaction[] {
  return blockchainLedger.filter(t => t.patientId === patientId || t.patientId === patientId.replace('pat-', 'MC-2026-'));
}

export function getTransactionByHash(hash: string): BlockchainTransaction | undefined {
  return blockchainLedger.find(t => t.hash === hash);
}

export function getTotalTransactionCount(): number {
  return blockchainLedger.length;
}

export function getTransactionStats() {
  return {
    createRecord: blockchainLedger.filter(t => t.transactionType === 'createRecord').length,
    grantConsent: blockchainLedger.filter(t => t.transactionType === 'grantConsent').length,
    revokeConsent: blockchainLedger.filter(t => t.transactionType === 'revokeConsent').length,
    emergencyAccess: blockchainLedger.filter(t => t.transactionType === 'emergencyAccess').length,
    auditAccess: blockchainLedger.filter(t => t.transactionType === 'auditAccess').length,
    updateRecord: blockchainLedger.filter(t => t.transactionType === 'updateRecord').length,
  };
}

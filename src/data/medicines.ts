export function getMedicineById(id: string): Medicine | undefined {
  return medicines.find(m => m.id === id);
}

export function searchMedicines(query: string): Medicine[] {
  return medicines.filter(
    m =>
      m.brandName.toLowerCase().includes(query.toLowerCase()) ||
      m.genericName.toLowerCase().includes(query.toLowerCase())
  );
}

export function getVerifiedMedicines(): Medicine[] {
  return medicines.filter(m => m.verificationStatus === 'Verified');
}

export function getSuspiciousMedicines(): Medicine[] {
  return medicines.filter(m => m.verificationStatus === 'Suspicious');
}

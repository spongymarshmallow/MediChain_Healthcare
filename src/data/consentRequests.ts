export function getConsentRequestsByPatient(patientId: string): ConsentRequest[] {
  return consentRequests.filter(r => r.patientId === patientId);
}

export function getPendingConsentRequests(patientId: string): ConsentRequest[] {
  return consentRequests.filter(r => r.patientId === patientId && r.status === 'Pending');
}

export function getAccessLogsByPatient(patientId: string): ConsentAccessLog[] {
  return consentAccessLogs.filter(l => l.patientId === patientId);
}

export function getConsentRequestById(id: string): ConsentRequest | undefined {
  return consentRequests.find(r => r.id === id);
}
